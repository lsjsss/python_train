# and 洁癖 一假俱假
print(True and True)
print(False and True)
print(True and False)
print(False and False)

print(1 and 5)
print(1 and 0)
print(0 and 5)

# or 乐观派 一好俱好
print(True or True)
print(False or True)
print(True or False)
print(False or False)

print(1 or 5)
print(1 or 0)
print(0 or 5)
print(0 or 0)

# not 运算 取反
print(not True)
print(not False)

# 复杂的短路运算
print(1 and 8 or True)
print(1 and 8 or False)
print(1 and False or False)


